#include <unistd.h>

int main(int ar, char **av)
{
    int i;
    int l;

    i = 1;
    while (i < ar)
    {
        l = 0;
        while (av[i][l])
        {
            write(1, &av[i][l], 1);
            l++;
        }
        write(1, "\n", 1);
        i++;
    }

    return (0);
}

